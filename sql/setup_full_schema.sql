BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'assigned_team_snapshots_report_id_key') THEN
        ALTER TABLE public.assigned_team_snapshots ADD CONSTRAINT assigned_team_snapshots_report_id_key UNIQUE (report_id);
