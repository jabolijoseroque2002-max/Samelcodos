CREATE OR REPLACE FUNCTION public.assign_report_personnel(
    p_report_id bigint,
    p_personnel_id bigint DEFAULT null,
    p_personnel_ids jsonb DEFAULT null
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, extensions
AS $$
DECLARE
    v_personnel public.personnel%ROWTYPE;
    v_report public.reports%ROWTYPE;
    v_personnel_team_name text;
    v_selected_ids bigint[] := ARRAY[]::bigint[];
    v_selected_personnel_id bigint;
    v_result jsonb;
BEGIN
    IF p_report_id IS NULL THEN
        RAISE EXCEPTION 'Report ID is required';
    END IF;

    SELECT r.*
    INTO v_report
    FROM public.reports r
    WHERE r.id = p_report_id
    LIMIT 1;

    IF v_report.id IS NULL THEN
        RAISE EXCEPTION 'Report with ID % not found', p_report_id;
    END IF;

    IF p_personnel_ids IS NOT NULL THEN
        IF jsonb_typeof(p_personnel_ids) <> 'array' THEN
            RAISE EXCEPTION 'Personnel IDs must be an array';
        END IF;

        SELECT coalesce(array_agg(DISTINCT raw.value::bigint), ARRAY[]::bigint[])
        INTO v_selected_ids
        FROM jsonb_array_elements_text(p_personnel_ids) AS raw(value)
        WHERE nullif(btrim(raw.value), '') IS NOT NULL;
    ELSIF p_personnel_id IS NOT NULL THEN
        v_selected_ids := ARRAY[p_personnel_id];
    END IF;

    IF coalesce(array_length(v_selected_ids, 1), 0) > 0 THEN
        IF nullif(btrim(coalesce(v_report.assigned_team, '')), '') IS NULL THEN
            RAISE EXCEPTION 'Assign a team to the report before assigning personnel';
        END IF;

        FOREACH v_selected_personnel_id IN ARRAY v_selected_ids LOOP
            SELECT *
            INTO v_personnel
            FROM public.personnel p
            WHERE p.id = v_selected_personnel_id
            LIMIT 1;

            IF v_personnel.id IS NULL THEN
                RAISE EXCEPTION 'Selected personnel was not found';
            END IF;

            IF coalesce(v_personnel.is_active, false) = false THEN
                RAISE EXCEPTION 'Selected personnel is inactive';
            END IF;

            SELECT t.name
            INTO v_personnel_team_name
            FROM public.teams t
            WHERE t.id = v_personnel.team_id
            LIMIT 1;

            IF nullif(btrim(coalesce(v_personnel_team_name, '')), '') IS NULL THEN
                RAISE EXCEPTION 'Selected personnel is not linked to a team';
            END IF;

            IF NOT EXISTS (
                SELECT 1
                FROM unnest(string_to_array(coalesce(v_report.assigned_team, ''), ',')) AS assigned_team_name
                WHERE lower(btrim(assigned_team_name)) = lower(btrim(v_personnel_team_name))
            ) THEN
                RAISE EXCEPTION 'Selected personnel does not belong to an assigned team for this report';
            END IF;
        END LOOP;
    END IF;

    DELETE FROM public.report_personnel_assignments rpa
    WHERE rpa.report_id = p_report_id
      AND NOT (rpa.personnel_id = ANY(v_selected_ids));

    IF coalesce(array_length(v_selected_ids, 1), 0) > 0 THEN
        INSERT INTO public.report_personnel_assignments (
            report_id,
            personnel_id,
            assignment_status,
            assigned_at,
            done_at,
            created_at,
            updated_at
        )
        SELECT
            p_report_id,
            selected_personnel_id,
            'assigned',
            now(),
            null,
            now(),
            now()
        FROM unnest(v_selected_ids) AS selected_personnel_id
        ON CONFLICT (report_id, personnel_id) DO UPDATE
        SET updated_at = now();
    END IF;

    PERFORM public.refresh_report_personnel_summary(p_report_id);

    SELECT to_jsonb(r.*)
    INTO v_result
    FROM public.reports r
    WHERE r.id = p_report_id
    LIMIT 1;

    RETURN v_result;
END;
$$;

DROP FUNCTION IF EXISTS public.mark_personnel_assignment_done(bigint, bigint);

CREATE OR REPLACE FUNCTION public.mark_personnel_assignment_done(
    p_report_id bigint,
    p_personnel_id bigint
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, extensions
AS $$
DECLARE
    v_result jsonb;
    v_updated_report_id bigint;
BEGIN
    IF p_report_id IS NULL THEN
        RAISE EXCEPTION 'Report ID is required';
    END IF;

    IF p_personnel_id IS NULL THEN
        RAISE EXCEPTION 'Personnel ID is required';
    END IF;

    UPDATE public.report_personnel_assignments rpa
    SET
        assignment_status = 'done',
        done_at = now(),
        updated_at = now()
    WHERE rpa.report_id = p_report_id
      AND rpa.personnel_id = p_personnel_id
    RETURNING rpa.report_id INTO v_updated_report_id;

    IF v_updated_report_id IS NULL THEN
        RAISE EXCEPTION 'Personnel assignment was not found';
    END IF;

    PERFORM public.refresh_report_personnel_summary(p_report_id);

    SELECT to_jsonb(r.*)
    INTO v_result
    FROM public.reports r
    WHERE r.id = p_report_id
    LIMIT 1;

    RETURN v_result;
END;
$$;

