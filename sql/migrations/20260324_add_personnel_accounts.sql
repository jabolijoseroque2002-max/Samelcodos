-- Adds personnel accounts, assignment support, and personnel login RPCs.
-- Safe to run multiple times.

begin;

create extension if not exists pgcrypto with schema extensions;

create or replace function public.update_updated_at_column()
returns trigger
language plpgsql
as $$
begin
  if to_jsonb(new) ? 'updated_at' then
    new := jsonb_populate_record(new, jsonb_build_object('updated_at', now()));
  end if;
  return new;
end;
$$;

alter table public.reports
  add column if not exists assigned_personnel text,
  add column if not exists assigned_personnel_id bigint;

create table if not exists public.personnel (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  full_name text not null,
  team_id bigint references public.teams(id) on delete set null,
  is_active boolean not null default true
);

alter table public.personnel
  add column if not exists created_at timestamptz not null default now(),
  add column if not exists updated_at timestamptz not null default now(),
  add column if not exists full_name text,
  add column if not exists team_id bigint,
  add column if not exists is_active boolean not null default true,
  add column if not exists email text,
  add column if not exists password_hash text,
  add column if not exists last_login_at timestamptz;

do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conname = 'personnel_team_id_fkey'
  ) then
    alter table public.personnel
      add constraint personnel_team_id_fkey
      foreign key (team_id) references public.teams(id) on delete set null;
  end if;
end
$$;

do $$
begin
  if not exists (
    select 1
    from pg_indexes
    where indexname = 'personnel_email_unique_idx'
  ) then
    create unique index personnel_email_unique_idx
      on public.personnel (lower(email))
      where nullif(btrim(email), '') is not null;
  end if;
end
$$;

alter table public.personnel enable row level security;
drop policy if exists "Allow public to view personnel" on public.personnel;
create policy "Allow public to view personnel" on public.personnel
  for select using (true);
drop policy if exists "Allow authenticated users to manage personnel" on public.personnel;
create policy "Allow authenticated users to manage personnel" on public.personnel
  for all using (true);

grant all on table public.personnel to anon, authenticated, service_role;
grant all on all sequences in schema public to anon, authenticated, service_role;

drop trigger if exists set_personnel_updated_at on public.personnel;
create trigger set_personnel_updated_at
before update on public.personnel
for each row
