do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conname = 'report_personnel_assignments_report_id_fkey'
  ) then
    alter table public.report_personnel_assignments
      add constraint report_personnel_assignments_report_id_fkey
      foreign key (report_id) references public.reports(id) on delete cascade;
  end if;
